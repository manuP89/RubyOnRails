
class Calculadora
  
  def initialize 
    
  end

  def suma (valor1, valor2)
    valor1 + valor2
  end
  
  def resta (valor1, valor2)
    valor1 - valor2
  end
  
  def multi (valor1, valor2)
    valor1 * valor2
  end
  
  def divis (valor1, valor2)
    valor1 / valor2
  end

end